
package ENTIDAD;

public class personalSituacionC {
    private int situacion;
    private String descripcion;
    private String abreviatua;
    private int estadoRegistro;

    /**
     * @return the situacion
     */
    public int getSituacion() {
        return situacion;
    }

    /**
     * @param situacion the situacion to set
     */
    public void setSituacion(int situacion) {
        this.situacion = situacion;
    }

    /**
     * @return the descripcion
     */
    public String getDescripcion() {
        return descripcion;
    }

    /**
     * @param descripcion the descripcion to set
     */
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    /**
     * @return the abreviatua
     */
    public String getAbreviatua() {
        return abreviatua;
    }

    /**
     * @param abreviatua the abreviatua to set
     */
    public void setAbreviatua(String abreviatua) {
        this.abreviatua = abreviatua;
    }

    /**
     * @return the estadoRegistro
     */
    public int getEstadoRegistro() {
        return estadoRegistro;
    }

    /**
     * @param estadoRegistro the estadoRegistro to set
     */
    public void setEstadoRegistro(int estadoRegistro) {
        this.estadoRegistro = estadoRegistro;
    }
}
