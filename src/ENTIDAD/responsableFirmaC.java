package ENTIDAD;

/**
 *
 * @author Administrador
 */
public class responsableFirmaC {
    private int tipoCertificado;
    private String persona;
    private String observacion;
    private int estadoResgistro;

    public responsableFirmaC() {
    }

   



  

    /**
     * @return the persona
     */
    public String getPersona() {
        return persona;
    }

    /**
     * @param persona the persona to set
     */
    public void setPersona(String persona) {
        this.persona = persona;
    }

    /**
     * @return the observacion
     */
    public String getObservacion() {
        return observacion;
    }

    /**
     * @param observacion the observacion to set
     */
    public void setObservacion(String observacion) {
        this.observacion = observacion;
    }

    /**
     * @return the estadoResgistro
     */
    public int getEstadoResgistro() {
        return estadoResgistro;
    }

    /**
     * @param estadoResgistro the estadoResgistro to set
     */
    public void setEstadoResgistro(int estadoResgistro) {
        this.estadoResgistro = estadoResgistro;
    }

    /**
     * @return the tipoCertificado
     */
    public int getTipoCertificado() {
        return tipoCertificado;
    }

    /**
     * @param tipoCertificado the tipoCertificado to set
     */
    public void setTipoCertificado(int tipoCertificado) {
        this.tipoCertificado = tipoCertificado;
    }

  
}
