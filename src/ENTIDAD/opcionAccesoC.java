
package ENTIDAD;

public class opcionAccesoC {
    private String Opcion;
    private String Seleccion;
    private String Descripcion;
    private String subDescripcion;
    private String url;
    private String formularioVB;

    /**
     * @return the Opcion
     */
    public String getOpcion() {
        return Opcion;
    }

    /**
     * @param Opcion the Opcion to set
     */
    public void setOpcion(String Opcion) {
        this.Opcion = Opcion;
    }

    /**
     * @return the Seleccion
     */
    public String getSeleccion() {
        return Seleccion;
    }

    /**
     * @param Seleccion the Seleccion to set
     */
    public void setSeleccion(String Seleccion) {
        this.Seleccion = Seleccion;
    }

    /**
     * @return the Descripcion
     */
    public String getDescripcion() {
        return Descripcion;
    }

    /**
     * @param Descripcion the Descripcion to set
     */
    public void setDescripcion(String Descripcion) {
        this.Descripcion = Descripcion;
    }

    /**
     * @return the formularioVB
     */
    public String getFormularioVB() {
        return formularioVB;
    }

    /**
     * @param formularioVB the formularioVB to set
     */
    public void setFormularioVB(String formularioVB) {
        this.formularioVB = formularioVB;
    }

    /**
     * @return the subDescripcion
     */
    public String getSubDescripcion() {
        return subDescripcion;
    }

    /**
     * @param subDescripcion the subDescripcion to set
     */
    public void setSubDescripcion(String subDescripcion) {
        this.subDescripcion = subDescripcion;
    }

    /**
     * @return the url
     */
    public String getUrl() {
        return url;
    }

    /**
     * @param url the url to set
     */
    public void setUrl(String url) {
        this.url = url;
    }
            
    
}
