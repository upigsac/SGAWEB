package ENTIDAD;
public class condicionLaboralC {
    private int condicionLaboral;
    private String descripcion;
    private String abreviatura;
    private int estadoRegistro;

    /**
     * @return the condicionLaboral
     */
    public int getCondicionLaboral() {
        return condicionLaboral;
    }

    /**
     * @param condicionLaboral the condicionLaboral to set
     */
    public void setCondicionLaboral(int condicionLaboral) {
        this.condicionLaboral = condicionLaboral;
    }

    /**
     * @return the descripcion
     */
    public String getDescripcion() {
        return descripcion;
    }

    /**
     * @param descripcion the descripcion to set
     */
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    /**
     * @return the abreviatura
     */
    public String getAbreviatura() {
        return abreviatura;
    }

    /**
     * @param abreviatura the abreviatura to set
     */
    public void setAbreviatura(String abreviatura) {
        this.abreviatura = abreviatura;
    }

    /**
     * @return the estadoRegistro
     */
    public int getEstadoRegistro() {
        return estadoRegistro;
    }

    /**
     * @param estadoRegistro the estadoRegistro to set
     */
    public void setEstadoRegistro(int estadoRegistro) {
        this.estadoRegistro = estadoRegistro;
    }
}
