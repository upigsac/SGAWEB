
package  ENTIDAD;

public class autorizacionC {
    
    private int tipoAutorizacion;
    private String descripcion;
    private String abreviatura;
    private int estado_registro;

    /**
     * @return the tipoAutorizacion
     */
    public int getTipoAutorizacion() {
        return tipoAutorizacion;
    }

    /**
     * @param tipoAutorizacion the tipoAutorizacion to set
     */
    public void setTipoAutorizacion(int tipoAutorizacion) {
        this.tipoAutorizacion = tipoAutorizacion;
    }

    /**
     * @return the descripcion
     */
    public String getDescripcion() {
        return descripcion;
    }

    /**
     * @param descripcion the descripcion to set
     */
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    /**
     * @return the abreviatura
     */
    public String getAbreviatura() {
        return abreviatura;
    }

    /**
     * @param abreviatura the abreviatura to set
     */
    public void setAbreviatura(String abreviatura) {
        this.abreviatura = abreviatura;
    }

    /**
     * @return the estado_registro
     */
    public int getEstado_registro() {
        return estado_registro;
    }

    /**
     * @param estado_registro the estado_registro to set
     */
    public void setEstado_registro(int estado_registro) {
        this.estado_registro = estado_registro;
    }
    
}
