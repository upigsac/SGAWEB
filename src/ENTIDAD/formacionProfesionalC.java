
package ENTIDAD;
public class formacionProfesionalC {
    private int formacionProfesional;
    private String descripcion;
    private String abreviatura;
    private int estadoRegistro;

    /**
     * @return the formacionProfesional
     */
    public int getFormacionProfesional() {
        return formacionProfesional;
    }

    /**
     * @param formacionProfesional the formacionProfesional to set
     */
    public void setFormacionProfesional(int formacionProfesional) {
        this.formacionProfesional = formacionProfesional;
    }

    /**
     * @return the descripcion
     */
    public String getDescripcion() {
        return descripcion;
    }

    /**
     * @param descripcion the descripcion to set
     */
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    /**
     * @return the abreviatura
     */
    public String getAbreviatura() {
        return abreviatura;
    }

    /**
     * @param abreviatura the abreviatura to set
     */
    public void setAbreviatura(String abreviatura) {
        this.abreviatura = abreviatura;
    }

    /**
     * @return the estadoRegistro
     */
    public int getEstadoRegistro() {
        return estadoRegistro;
    }

    /**
     * @param estadoRegistro the estadoRegistro to set
     */
    public void setEstadoRegistro(int estadoRegistro) {
        this.estadoRegistro = estadoRegistro;
    }
}
