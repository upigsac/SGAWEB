
package ENTIDAD;

public class personalCatedraC {
    private String personal;
    private String centroEducativo;
    private int catedra;
    private int meses;
    private boolean dictar;
    private int estadoRegistro;

    /**
     * @return the personal
     */
    public String getPersonal() {
        return personal;
    }

    /**
     * @param personal the personal to set
     */
    public void setPersonal(String personal) {
        this.personal = personal;
    }

    /**
     * @return the centroEducativo
     */
    public String getCentroEducativo() {
        return centroEducativo;
    }

    /**
     * @param centroEducativo the centroEducativo to set
     */
    public void setCentroEducativo(String centroEducativo) {
        this.centroEducativo = centroEducativo;
    }

    /**
     * @return the catedra
     */
    public int getCatedra() {
        return catedra;
    }

    /**
     * @param catedra the catedra to set
     */
    public void setCatedra(int catedra) {
        this.catedra = catedra;
    }

    /**
     * @return the meses
     */
    public int getMeses() {
        return meses;
    }

    /**
     * @param meses the meses to set
     */
    public void setMeses(int meses) {
        this.meses = meses;
    }

    /**
     * @return the dictar
     */
    public boolean isDictar() {
        return dictar;
    }

    /**
     * @param dictar the dictar to set
     */
    public void setDictar(boolean dictar) {
        this.dictar = dictar;
    }

    /**
     * @return the estadoRegistro
     */
    public int getEstadoRegistro() {
        return estadoRegistro;
    }

    /**
     * @param estadoRegistro the estadoRegistro to set
     */
    public void setEstadoRegistro(int estadoRegistro) {
        this.estadoRegistro = estadoRegistro;
    }
}
