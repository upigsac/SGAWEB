
package  ENTIDAD;

public class carpetaInstitucionC {
    private int institucion;
    private String periodo;
    private int carpeta;
    private int pesoMaximo;
    private int pesoMinimo;
    private int estadoRegistro;

    /**
     * @return the institucion
     */
    public int getInstitucion() {
        return institucion;
    }

    /**
     * @param institucion the institucion to set
     */
    public void setInstitucion(int institucion) {
        this.institucion = institucion;
    }

    /**
     * @return the periodo
     */
    public String getPeriodo() {
        return periodo;
    }

    /**
     * @param periodo the periodo to set
     */
    public void setPeriodo(String periodo) {
        this.periodo = periodo;
    }

    /**
     * @return the carpeta
     */
    public int getCarpeta() {
        return carpeta;
    }

    /**
     * @param carpeta the carpeta to set
     */
    public void setCarpeta(int carpeta) {
        this.carpeta = carpeta;
    }

    /**
     * @return the pesoMaximo
     */
    public int getPesoMaximo() {
        return pesoMaximo;
    }

    /**
     * @param pesoMaximo the pesoMaximo to set
     */
    public void setPesoMaximo(int pesoMaximo) {
        this.pesoMaximo = pesoMaximo;
    }

    /**
     * @return the pesoMinimo
     */
    public int getPesoMinimo() {
        return pesoMinimo;
    }

    /**
     * @param pesoMinimo the pesoMinimo to set
     */
    public void setPesoMinimo(int pesoMinimo) {
        this.pesoMinimo = pesoMinimo;
    }

    /**
     * @return the estadoRegistro
     */
    public int getEstadoRegistro() {
        return estadoRegistro;
    }

    /**
     * @param estadoRegistro the estadoRegistro to set
     */
    public void setEstadoRegistro(int estadoRegistro) {
        this.estadoRegistro = estadoRegistro;
    }
}
